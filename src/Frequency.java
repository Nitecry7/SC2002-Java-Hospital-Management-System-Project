/**
 * Enumeration representing various frequencies for medication administration.
 */
public enum Frequency {

    /**
     * Pro re nata (as needed).
     */
    PRN,

    /**
     * Bis in die (twice a day).
     */
    BD,

    /**
     * Ter in die (three times a day).
     */
    TDS,

    /**
     * Quater in die (four times a day).
     */
    QDS,

    /**
     * Mane (in the morning).
     */
    MANE,

    /**
     * Nocte (at night).
     */
    NOCTE
}
